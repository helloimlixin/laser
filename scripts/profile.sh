debug_profile_cases() {
  DEBUG_CASES=(
    "p2s2_k24_d4k|true|2|2|tile|24|16"
    "p4s4_k24_d4k|true|4|4|tile|24|16"
    "p8s8_k24_d4k|true|8|8|tile|24|16"
    "p4s2h_k24_d4k|true|4|2|hann|24|16"
    "p8s4h_k24_d4k|true|8|4|hann|24|16"
    "nopatch_k16_d4k|false|0|0|tile|16|16"
  )
}

debug_profile_case_label() {
  local raw="${1:-case}"
  printf '%s\n' "${raw%%_*}"
}

debug_profile_dict_label() {
  local raw="${1:-0}"
  if [[ "$raw" =~ ^[0-9]+$ ]] && (( raw % 1024 == 0 )) && (( raw >= 1024 )); then
    printf 'k%dk\n' "$(( raw / 1024 ))"
  else
    printf 'k%s\n' "$raw"
  fi
}

debug_profile_run_family() {
  printf '%s\n' "${RUN_FAMILY:-chq256-${STAGE1_EPOCHS}x${STAGE2_EPOCHS}}"
}

debug_profile_job_family() {
  printf '%s\n' "${JOB_FAMILY:-c${STAGE1_EPOCHS}${STAGE2_EPOCHS}}"
}

debug_profile_slug() {
  local dict_size="${1:?dict_size required}"
  local case_name="${2:?case_name required}"
  local family
  family="$(debug_profile_run_family)"
  printf '%s-%s-%s\n' \
    "$family" \
    "$(debug_profile_dict_label "$dict_size")" \
    "$(debug_profile_case_label "$case_name")"
}
