export STAGE1_DICT_LOSS_WARMUP_EPOCHS="${STAGE1_DICT_LOSS_WARMUP_EPOCHS:-0}"
export STAGE1_COMMITMENT_LOSS_WEIGHT="${STAGE1_COMMITMENT_LOSS_WEIGHT:-nan}"
export STAGE1_COMMITMENT_LOSS_WEIGHT_START="${STAGE1_COMMITMENT_LOSS_WEIGHT_START:-nan}"
export STAGE1_COMMITMENT_LOSS_WARMUP_EPOCHS="${STAGE1_COMMITMENT_LOSS_WARMUP_EPOCHS:-0}"
export STAGE1_COHERENCE_WEIGHT="${STAGE1_COHERENCE_WEIGHT:-0.0}"
export STAGE1_COHERENCE_WEIGHT_START="${STAGE1_COHERENCE_WEIGHT_START:-nan}"
export STAGE1_COHERENCE_WARMUP_EPOCHS="${STAGE1_COHERENCE_WARMUP_EPOCHS:-0}"
