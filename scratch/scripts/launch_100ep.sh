export STAGE1_BOTTLENECK_WEIGHT_START="${STAGE1_BOTTLENECK_WEIGHT_START:-1.0}"
export STAGE1_BOTTLENECK_WARMUP_EPOCHS="${STAGE1_BOTTLENECK_WARMUP_EPOCHS:-0}"
export STAGE1_DICT_LOSS_WEIGHT="${STAGE1_DICT_LOSS_WEIGHT:-nan}"
