export STAGE1_DICT_MIN_LR_RATIO="${STAGE1_DICT_MIN_LR_RATIO:-0.05}"
export STAGE1_DICT_GRAD_CLIP="${STAGE1_DICT_GRAD_CLIP:-0.05}"
